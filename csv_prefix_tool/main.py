# =============================================================================
# main.py — CSV Prefix Tool 진입점(Entry Point)
# =============================================================================
#
# ★ 이 파일의 역할
#   프로그램을 실행할 때 가장 먼저 실행되는 파일입니다.
#   "진입점"(Entry Point)이라고 부르며, 보통 프로젝트마다 딱 하나만 있습니다.
#
# ★ 파일 구성
#   이 프로젝트는 여러 파일로 나뉘어 있습니다:
#     - main.py        ← 지금 이 파일 (실행 시작점)
#     - app.py         ← 메인 윈도우(창) 클래스
#     - grid_widget.py ← CSV 데이터를 입력하는 표(그리드) 위젯
#     - output_panel.py← 결과를 보여주는 출력 패널 위젯
#     - theme.py       ← 색상, 폰트 등 디자인 설정
#
# =============================================================================

# ── import: 다른 파일의 코드를 가져오기 ──────────────────────────────────────
# 파이썬에서 'from 파일 import 클래스/함수'로 다른 파일의 코드를 사용합니다.
# 여기서는 app.py 파일에서 CSVPrefixApp 클래스를 가져옵니다.
from app import CSVPrefixApp


# ── if __name__ == "__main__": ─────────────────────────────────────────────
# 파이썬의 특별한 관용구(idiom)입니다.
#
# __name__ 은 파이썬이 자동으로 설정하는 특수 변수입니다:
#   - 이 파일을 직접 실행하면 → __name__ == "__main__"  (True)
#   - 다른 파일이 이 파일을 import하면 → __name__ == "main" (False)
#
# 이 조건문 덕분에, 이 파일을 직접 실행할 때만 앱이 실행되고
# 실수로 import해도 앱이 갑자기 실행되지 않습니다.
if __name__ == "__main__":

    # CSVPrefixApp() : app.py에 정의된 클래스의 인스턴스(객체)를 만듭니다.
    # 클래스란? 설계도(틀)이고, 인스턴스란? 그 설계도로 만든 실체입니다.
    # 예) CSVPrefixApp = 자동차 설계도, app = 실제 자동차 한 대
    app = CSVPrefixApp()

    # mainloop() : tkinter GUI 앱의 핵심 메서드입니다.
    # 이 함수를 호출하면 프로그램이 무한 루프에 진입해서
    # 사용자의 마우스 클릭, 키 입력 등의 이벤트를 기다립니다.
    # 창을 닫으면 mainloop()가 종료되고 프로그램도 끝납니다.
    app.mainloop()
